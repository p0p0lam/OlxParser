public class TopHeaderLabels {
    private String category_icon;
    private String location_label;
    private int parameters_count;

    public String getCategory_icon() {
        return category_icon;
    }

    public void setCategory_icon(String category_icon) {
        this.category_icon = category_icon;
    }

    public String getLocation_label() {
        return location_label;
    }

    public void setLocation_label(String location_label) {
        this.location_label = location_label;
    }

    public int getParameters_count() {
        return parameters_count;
    }

    public void setParameters_count(int parameters_count) {
        this.parameters_count = parameters_count;
    }
}
